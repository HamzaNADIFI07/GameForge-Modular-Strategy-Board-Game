package ressource;

/**
 * L'énumération enumRessource représente les différents types de ressources disponibles dans le jeu.
 * Chaque ressource correspond à un type de tuile spécifique qui peut la produire.
 */
public enum Ressource { 
    Ble, // Ressource produite par les champs
    Minerai, // Ressource produite par les montagnes
    Bois, // Ressource produite par les forêts
    Moutons; // Ressource produite par les pâturages
}
